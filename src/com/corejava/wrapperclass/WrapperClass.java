package com.corejava.wrapperclass;

public class WrapperClass {
    public static void main(String[] args) {
        // Primitive Data Type and Wrapper Class
        /*
            - byte = Byte
            - short -> Short
            - int   -> Integer
            - long  -> Long
            - float  -> Float
            - double  -> Double
            - boolean  -> Boolean
            - char  -> Character
         */

        // int age = 20;
        Integer age = 20;
        int balance = Integer.parseInt("400");

        System.out.println(age);
        System.out.println(balance);

    }
}
