package com.corejava.operators.assignmentoperator.simpleassignmentoperator;

public class SimpleAssignmentOperator {
    public static void main(String[] args) {
        //Assignment operators are used to assign values to a variable.

        // Declaring Variables
        int age;
        String name;

        // Assigning Values with the help of Assignment Operator (=)
        age = 20;
        name = "Krishna Bishowkarma";

        // Printing the values
        System.out.println("My Name is " + name + " and I am " + age + " years old.");
    }
}
