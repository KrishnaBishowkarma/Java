package com.corejava.operators.comparisionoperator;

public class IsEqualToOperator {
    public static void main(String[] args) {

        int a = 20, b = 30, c = 90, d = 30;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("d = " + d);

        // Performing is equal to operator ==
        System.out.println("Value of a is equal to Value of c is : " + (a == c));
        System.out.println("Value of b is equal to Value of d is : " + (b == d));
    }
}
