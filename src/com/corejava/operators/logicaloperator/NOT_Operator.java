package com.corejava.operators.logicaloperator;

public class NOT_Operator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // if the condition is false, the operation returns true and when the condition is true, the operation returns false.

        System.out.println("!(a<=b) : " + !(a<=b));
        System.out.println("!(a>=b) : " + !(a>=b));
    }
}
