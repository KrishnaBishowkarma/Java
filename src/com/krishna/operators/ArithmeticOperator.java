package com.krishna.operators;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int numberOne = 100;
        int numberTwo = 15;

        // Addition +
        int sum = numberOne + numberTwo;
        System.out.println("The sum of two numbers is : " + sum);

        // Subtraction -
        int difference = numberOne - numberTwo;
        System.out.println("The difference between tow numbers is : " + difference);

        // Multiplication *
        int multiplication = numberOne * numberTwo;
        System.out.println("The multiplication of two numbers is : " + multiplication);

        // Division /
        int division  = numberOne / numberTwo;
        System.out.println("The division of two numbers is : " + division);

        // Modulus %
        int remainder = numberOne % numberTwo;
        System.out.println("The remainder between two numbers is : " + remainder);
    }
}
