package com.krishna.dataTypes;

public class PrimitiveDataTypes {
    public static void main(String[] args){
        //here are all the lists of primitive data types and their uses.

        // Integer or Int
        int a = 10;
        int b = 20;

        int sum  = a +b;
        System.out.println("Sum of " + a + " and " + b + " is = " + sum);


        // Float
        float f = 3.14f;

        //Double
        double d = 3.14;

        //Byte
        byte bt = 2;

        //Short
        short s = 1_20;

        //Character
        char ch = 'A';

        //Boolean
        boolean isAdult = false;

        //Long
        long l = 12_000_000_000L;
    }
}
